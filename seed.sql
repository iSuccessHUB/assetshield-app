  <li>You need flexibility to modify the structure</li>
  <li>You want pass-through tax treatment</li>
  <li>Your asset protection needs are moderate</li>
</ul>

<h2>When to Choose a Trust</h2>

<h3>Trusts are ideal when:</h3>
